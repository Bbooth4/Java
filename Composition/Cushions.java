public class Cushions {
  private String fabric;
  private String colour;

  public Cushions(String fabric, String colour) {
    this.fabric = fabric;
    this.colour = colour;
  }

  public void setFabric(String fabric) {
    this.fabric = fabric;
  }

  public void setColour(String colour) {
    this.colour = colour;
  }
}